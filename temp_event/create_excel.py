from openpyxl import Workbook
from openpyxl.styles import Font, PatternFill, Alignment, Border, Side

wb = Workbook()

# 样式定义
header_font = Font(name='Arial', bold=True, color='FFFFFF', size=11)
header_fill = PatternFill('solid', fgColor='4A1D7A')
cat_font = Font(name='Arial', bold=True, color='1E1B4B', size=11)
cat_fill = PatternFill('solid', fgColor='EDE9FE')
normal_font = Font(name='Arial', size=10)
wrap_align = Alignment(wrap_text=True, vertical='center')
center_align = Alignment(horizontal='center', vertical='center', wrap_text=True)
thin_border = Border(
    left=Side(style='thin', color='E0E0E0'),
    right=Side(style='thin', color='E0E0E0'),
    top=Side(style='thin', color='E0E0E0'),
    bottom=Side(style='thin', color='E0E0E0')
)

def setup_sheet(ws, title, headers):
    ws.title = title
    for col, h in enumerate(headers, 1):
        cell = ws.cell(row=1, column=col, value=h)
        cell.font = header_font
        cell.fill = header_fill
        cell.alignment = center_align
        cell.border = thin_border
    ws.column_dimensions['A'].width = 8
    ws.column_dimensions['B'].width = 18
    ws.column_dimensions['C'].width = 12
    ws.column_dimensions['D'].width = 40
    ws.column_dimensions['E'].width = 40
    ws.freeze_panes = 'A2'

def add_row(ws, row, num, cat, subcat, desc, example, is_cat=False):
    vals = [num, cat, subcat, desc, example]
    for col, v in enumerate(vals, 1):
        cell = ws.cell(row=row, column=col, value=v)
        cell.border = thin_border
        cell.alignment = wrap_align
        if is_cat:
            cell.font = cat_font
            cell.fill = cat_fill
        else:
            cell.font = normal_font

